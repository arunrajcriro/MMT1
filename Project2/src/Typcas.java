
public class Typcas {
	public static void main(String[] args) {
		
		
		int x=1057;
		float y=x;
		System.out.println(y);
		
		
		short m=234;
		double n=m;
		System.out.println(n);
		
		float a=123.45f;
		int b=(int)a;
		System.out.println(b);
		
		double c=20545.3456712d;
		long d=(long)c;
		System.out.println(d);
		
	}

}
